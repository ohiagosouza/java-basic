public class TiposVariaveis {
    public static void main(String[] args) throws Exception {
        int inteiro = 1507; // Números inteiros
        double pontoFlutuante = 2575.99; // Números com ponto flutuante
        String string = "Uma string"; // Uma string, ou seja, conjunto de caracteres
        long longNumber = Long.MAX_VALUE; // Número máximo do tipo long
        char umChar = 'S'; // Usado quando será apenas UM caractere

        System.out.println(inteiro);
        System.out.println(pontoFlutuante);
        System.out.println(string);
        System.out.println(longNumber);
        System.out.println(umChar);
    }
}
