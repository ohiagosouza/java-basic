public class Operadores {
  public static void main(String[] args) {
    String nome = "Hiago";
    String sobrenome = "Souza";
    String nomeCompleto = nome + sobrenome; // Realiza a concatenação da variável nome e sobrenome
    System.out.println(nomeCompleto); // Imprime a concatenação das variáveis -> HiagoSouza
  }
}
